
/*DATABASE CREATION*/
CREATE DATABASE IF NOT EXISTS motorph_employee_management_system;

/*TO ACCESS THE DATABASE*/
USE motorph_employee_management_system;

/*=======================================================================================
CREATE TABLES: CORE EMPLOYEE MANAGEMENT 
======================================================================================== */
CREATE TABLE IF NOT EXISTS department (
    department_id INT NOT NULL AUTO_INCREMENT,
    name ENUM (
		'Executive',
		'IT',
		'HR',
		'Finance',
		'Sales & Marketing',
		'Operations/Logistics',
		'Customer Relations')
    NOT NULL,
    description TEXT NOT NULL,
    PRIMARY KEY (department_id)
);

CREATE TABLE IF NOT EXISTS employee_position (
    position_id INT NOT NULL AUTO_INCREMENT,
    department_id INT NOT NULL,
    name ENUM (
		'Chief Executive Officer',
		'Chief Operating Officer',
		'Chief Finance Officer',
		'Chief Marketing Officer',
		'IT Operations and Systems',
		'HR Manager',
		'HR Team Leader',
		'HR Rank and File',
		'Accounting Head',
		'Payroll Manager',
		'Payroll Team Leader',
		'Payroll Rank and File',
		'Account Manager',
		'Account Team Leader',
		'Account Rank and File',
		'Sales & Marketing',
		'Supply Chain and Logistics',
		'Customer Service and Relations')
    NOT NULL,
    description VARCHAR(215) NOT NULL,
    PRIMARY KEY (position_id),
    FOREIGN KEY (department_id) REFERENCES department(department_id)
);

CREATE TABLE IF NOT EXISTS status (
    status_id INT NOT NULL AUTO_INCREMENT,
    status_name ENUM (
    'Regular',
    'Probationary')
    NOT NULL,
    PRIMARY KEY (status_id)
);

CREATE TABLE IF NOT EXISTS employee_profile (
    employee_id INT NOT NULL AUTO_INCREMENT,
    last_name VARCHAR(50) NOT NULL,
    first_name VARCHAR(50) NOT NULL,
    birthday DATE NOT NULL,
    phone_number VARCHAR(13),
    hire_date DATE NOT NULL,
    email VARCHAR(25) NOT NULL,
    supervisor_name VARCHAR (100) NOT NULL,
    department_id INT NOT NULL,
    position_id INT NOT NULL,
    status_id INT NOT NULL,
    is_active BOOLEAN NOT NULL,
    PRIMARY KEY (employee_id),
    FOREIGN KEY (department_id) REFERENCES department(department_id),
    FOREIGN KEY (position_id) REFERENCES employee_position(position_id),
    FOREIGN KEY (status_id) REFERENCES status(status_id)
);

CREATE TABLE IF NOT EXISTS address (
    address_id INT NOT NULL AUTO_INCREMENT,
    house_no VARCHAR(100),
    street VARCHAR(100),
    barangay VARCHAR(100),
    city VARCHAR(100) NOT NULL,
    province VARCHAR(100) NOT NULL,
    postal_code VARCHAR(4) NOT NULL,
    country VARCHAR(100) NOT NULL DEFAULT 'Philippines',
    PRIMARY KEY (address_id)
);

